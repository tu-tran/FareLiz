namespace SkyDean.FareLiz.WinForm.Components.Controls.DatePicker.Renderer
{
    /// <summary>The header states.</summary>
    public enum MonthCalendarHeaderState
    {
        /// <summary>Indicates the default RequestState.</summary>
        Default, 

        /// <summary>Indicates the mouse over RequestState.</summary>
        Active, 

        /// <summary>Indicates the month name is in mouse over RequestState.</summary>
        MonthNameActive, 

        /// <summary>Indicates the month name is selected.</summary>
        MonthNameSelected, 

        /// <summary>Indicates the year is in mouse over RequestState.</summary>
        YearActive, 

        /// <summary>Indicates the year is selected.</summary>
        YearSelected, 

        /// <summary>Indicates the arrow is in mouse over RequestState.</summary>
        ArrowActive
    }
}